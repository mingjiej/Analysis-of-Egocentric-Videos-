

public class TransformParam {
	private double dx;
	private double dy;
	private double da;
	public TransformParam(double dx, double dy, double da) {
		this.da = da;
		this.dx = dx;
		this.dy = dy;
	}
	public double getDx() {
		return dx;
	}

	public double getDy() {
		return dy;
	}

	public double getDa() {
		return da;
	}
}
